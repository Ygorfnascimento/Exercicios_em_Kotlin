fun main () {
    val dia = 3

    when (dia) {
        1  -> println ("Domingo")
        2  -> println ("Segunda-Feira")
        3  -> println ("Terça-Feira")
        4  -> println ("Quarta-Feira")
        5  -> println ("Quinta-Feira")
        6  -> println ("Sexta-Feira")
        7  -> println ("Sábado")
        else -> println ("Dia inválido")

    }
}
